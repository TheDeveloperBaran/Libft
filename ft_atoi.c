#include "libft.h"

int	ft_atoi(const char *s)
{
	int		i;
	long	res;
	int		sign;

	res = 0;
	sign = 1;
	i = 0;
	while ((s[i] <= 13 && s[i] >= 9) || (s[i] == ' '))
		i++;
	if (s[i] == '-' || s[i] == '+')
	{
		if (s[i] == '-')
			sign *= -1;
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		res = res * 10 + (s[i] - '0') * sign;
		if (res > 2147483647)
			return (-1);
		if (res < -2147483648)
			return (0);
		i++;
	}
	return ((int)res);
}
